package sunny;

public class ArithmeticOperator {

	public static void main(String[] args) {
		byte a=12;
		short s=123;
		int i=1234;
		long l=123456789;
		float f=1.2f;
		double d=1.23456789;
		System.out.println(a+s); //addition
		System.out.println(l-i); //subtraction
		System.out.println(f*d); //multiplication
		System.out.println(i/s); //divison
		System.out.println(l%i); //modulus
		
	}
}

		
		
		
		
		

	


